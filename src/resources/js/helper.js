function inIframe() {
    try {
        return window.self !== window.top;
    } catch (e) {
        return true;
    }
}

function redirect(url) {
    window.location = url;
}

function refresh(checkParent) {
    if (typeof checkParent === 'undefined') {
        checkParent = true;
    }
    if (checkParent && inIframe()) {
        window.top.location = window.top.location;
    } else {
        window.location = window.location;
    }
}

function escapeHTML(text) {
    return text.replace(/\&/g, '&amp;').replace(/\</g, '&lt;').replace(/\>/g, '&gt;');
}

function bytesToSize(bytes) {
    var sizes = ['Bytes', 'KB', 'MB', 'GB', 'TB'];
    if (bytes === 0) return '0 Bytes';
    var i = parseInt(Math.floor(Math.log(bytes) / Math.log(1024)));
    return Math.round(bytes / Math.pow(1024, i), 2) + ' ' + sizes[i];
}

function substr_count(haystack, needle, offset, length) {
    var pos = 0,
        cnt = 0;
    haystack += '';
    needle += '';
    if (isNaN(offset)) {
        offset = 0;
    }
    if (isNaN(length)) {
        length = 0;
    }
    offset--;
    while ((offset = haystack.indexOf(needle, offset + 1)) !== -1) {
        if (length > 0 && (offset + needle.length) > length) {
            return false;
        } else {
            cnt++;
        }
    }
    return cnt;
}

function csvStringToArray(string) {
    if (substr_count(string, ',') === 0) {
        return [string];
    }
    return string.split(',');
}

function GetURLParameter(sParam) {
    var sPageURL = window.location.search.substring(1);
    var sURLVariables = sPageURL.split('&');
    for (var i = 0; i < sURLVariables.length; i++) {
        var sParameterName = sURLVariables[i].split('=');
        if (sParameterName[0] === sParam) {
            return sParameterName[1];
        }
    }
    return '';
}

function removeParam(key, sourceURL) {
    var rtn = sourceURL.split("?")[0],
        param,
        params_arr = [],
        queryString = (sourceURL.indexOf("?") !== -1) ? sourceURL.split("?")[1] : "";
    if (queryString !== "") {
        params_arr = queryString.split("&");
        for (var i = params_arr.length - 1; i >= 0; i -= 1) {
            param = params_arr[i].split("=")[0];
            if (param === key) {
                params_arr.splice(i, 1);
            }
        }
        rtn = rtn + "?" + params_arr.join("&");
    }
    return rtn;
}

function addParam(key, value, sourceURL) {
    var urlParts = sourceURL.split("?");

    if(urlParts.length === 1) {
        paramString = key + '=' + value;
    } else {
        paramString = urlParts[1] + '&'+ key + '=' + value;
    }

    return urlParts[0] + '?' + paramString;
}

